package com.nutritrack.lifestyleservice.model;

/**
 * Enum representing different types of lifestyle activities that can be tracked.
 */
public enum LifestyleType {
    /**
     * Step count tracking
     */
    STEPS,
    
    /**
     * Water intake tracking (in milliliters)
     */
    WATER,
    
    /**
     * Sleep duration tracking (in minutes)
     */
    SLEEP,
    
    /**
     * Jogging duration tracking (in minutes)
     */
    JOGGING,
    
    /**
     * Running duration tracking (in minutes)
     */
    RUNNING,
    
    /**
     * Meditation duration tracking (in minutes)
     */
    MEDITATION
}
